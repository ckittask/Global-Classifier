CREATE TABLE public.classification_results (
    id int8 NOT NULL GENERATED BY DEFAULT AS IDENTITY,
    chat_id VARCHAR(255) NOT NULL,
    inference_id VARCHAR(255) NOT NULL,
    target_agencies JSONB NOT NULL,
    classification_timestamp TIMESTAMP WITH TIME ZONE DEFAULT CURRENT_TIMESTAMP,
    created_at TIMESTAMP WITH TIME ZONE DEFAULT CURRENT_TIMESTAMP,
    CONSTRAINT classification_results_pkey PRIMARY KEY (id),
    CONSTRAINT unique_inference_id UNIQUE (inference_id)
);